public class MathTest {

	public static void main(String[] args) {
		System.out.printf("sqrt(900.0)=%.2f\n", Math.sqrt(900.0));
		System.out.printf("cos(900.0)=%.2f\n", Math.cos(900.0));
		System.out.printf("log(900.0)=%.2f\n", Math.log(900.0));
		System.out.printf("floor(9.25)=%.2f\n", Math.floor(9.25));
		System.out.println("PI=" + Math.PI);
	}

}
